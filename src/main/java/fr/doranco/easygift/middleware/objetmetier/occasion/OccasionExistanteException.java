package fr.doranco.easygift.middleware.objetmetier.occasion;

public class OccasionExistanteException extends Exception {

	private static final long serialVersionUID = 1L;

	public OccasionExistanteException() {
		super();
	}

	public OccasionExistanteException(String message, Throwable cause) {
		super(message, cause);
	}

	public OccasionExistanteException(String message) {
		super(message);
	}

	public OccasionExistanteException(Throwable cause) {
		super(cause);
	}

}
